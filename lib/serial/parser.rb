################################################################################
module Serial
  ################################################################################
  class Parser
  
    ################################################################################
    attr_reader :query, :key
    
    ################################################################################
    def initialize(options = {})
      @query = options[:query]
      parse
    end  
    
    ################################################################################
    def parse
      @key = @query.split('!')[0].sub '#', ''
    end
    
    ################################################################################
    private :parse
  end
end
