################################################################################
module Serial
  ################################################################################
  class Parser
  
    ################################################################################
    attr_accessor :query, :device_identification, :action_command, :command_type
    
    ################################################################################
    def initialize(options => {})
      @query = options[:query]
      parse
    end  
    
    ################################################################################
    def parse      
      @command_type, @device_identification, @action_command = @query.split '!'
    end
    
    ################################################################################
    private :parse
  end
end
